class AdamOptimizer:
    def __init__(self, optimizer_class, parameters, lr, betas):
        self.optimizer_class = optimizer_class
        self.parameters = parameters
        self.lr = lr
        self.betas = betas

    def create_optimizer(self):
        return self.optimizer_class(self.parameters, lr=self.lr, betas=self.betas)

